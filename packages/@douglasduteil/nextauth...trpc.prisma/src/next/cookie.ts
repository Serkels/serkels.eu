import type { CookiesOptions } from "next-auth";

//
// Inspired by https://github.com/nextauthjs/next-auth/blob/main/packages/core/src/lib/cookie.ts
// \from https://github.com/nextauthjs/next-auth/blob/d0f4b4a05e3cea09ee9888d44c8044beee2d9285/packages/core/src/lib/cookie.ts
